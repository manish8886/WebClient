public class CountryInfo {
    private String unCode;
    private String nameEN;
    private String nameCapital;
    private String population;
    private String gdpTotal;
    private String landarea;
    private String hdi;

    /**
     * @return the unCode
     */
    public String getUnCode() {
        return unCode;
    }

    /**
     * @param unCode the unCode to set
     */
    public void setUnCode(String unCode) {
        this.unCode = unCode;
    }

    /**
     * @return the nameEN
     */
    public String getNameEN() {
        return nameEN;
    }

    /**
     * @param nameEN the nameEN to set
     */
    public void setNameEN(String nameEN) {
        this.nameEN = nameEN;
    }

    /**
     * @return the nameCapital
     */
    public String getNameCapital() {
        return nameCapital;
    }

    /**
     * @param nameCapital the nameCapital to set
     */
    public void setNameCapital(String nameCapital) {
        this.nameCapital = nameCapital;
    }

    /**
     * @return the population
     */
    public String getPopulation() {
        return population;
    }

    /**
     * @param population the population to set
     */
    public void setPopulation(String population) {
        this.population = population;
    }

    /**
     * @return the gdpTotal
     */
    public String getGdpTotal() {
        return gdpTotal;
    }

    /**
     * @param gdpTotal the gdpTotal to set
     */
    public void setGdpTotal(String gdpTotal) {
        this.gdpTotal = gdpTotal;
    }

    /**
     * @return the landarea
     */
    public String getLandarea() {
        return landarea;
    }

    /**
     * @param landarea the landarea to set
     */
    public void setLandarea(String landarea) {
        this.landarea = landarea;
    }

    /**
     * @return the hdi
     */
    public String getHdi() {
        return hdi;
    }

    /**
     * @param hdi the hdi to set
     */
    public void setHdi(String hdi) {
        this.hdi = hdi;
    }
}
